@register(prefix="250397")
def Depth(depth_inicial):
  # Função de detecção de borda e função de blur
  depth_bordas = DeteccaoBordas(depth_inicial)
  depth_blur = Blur(depth_inicial)

  # Auxiliar para realizar blur
  depth_aux = depth_bordas <= 50
  depth_aux = np.array([depth_aux, depth_aux, depth_aux])
  depth_aux = depth_aux.transpose(1, 2, 0)

  # Aplicação do efeito
  depth_fim = np.where(depth_aux, depth_blur, depth_inicial)

  # Plotagem
  fig, axs = plt.subplots(2, 2, figsize = (12, 4))
  plt.tight_layout()

  plt.subplot(141)
  plt.imshow(depth_inicial)
  plt.title('Original')

  plt.subplot(143)
  plt.imshow(depth_bordas, cmap = 'gray')
  plt.title('Bordas')

  plt.subplot(142)
  plt.imshow(depth_blur)
  plt.title('Blur')

  plt.subplot(144)
  plt.imshow(depth_fim)
  plt.title('Final')

  return depth_fim

depth_fim = Depth(depth)

